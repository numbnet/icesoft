package org.icefaces.UIDataText;


/**
 *
 */
public class StyleHolder {

    private String style;

    public StyleHolder(String style) {
        this.style = style;
    }

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }

    public String toString() {
        return style;
    } 
}
