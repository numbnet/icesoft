package com.icesoft.faces.presenter.util;

import java.security.MessageDigest;
import java.security.NoSuchAlgorithmException;

import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;

/**
 * Class used to generically provide various encryption techniques
 * For example, a String can be passed to the md5 method to get the md5sum
 * value returned.
 */
public class Encrypter {
    private static Log log = LogFactory.getLog(Encrypter.class);

    /**
     * Method to encrypt a String using the md5 algorithm
     *
     * @param original to encrypt
     * @return the encrypted string, or the original on error
     */
    public static String md5(String original) {
        if ((original == null) || (original.length() == 0)) {
            return original;
        }

        StringBuffer hexString = new StringBuffer();

        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            md.update(original.getBytes());
            byte[] hash = md.digest();

            for (int i = 0; i < hash.length; i++) {
                if ((0xff & hash[i]) < 0x10) {
                    hexString.append("0")
                            .append(Integer.toHexString((0xFF & hash[i])));
                } else {
                    hexString.append(Integer.toHexString(0xFF & hash[i]));
                }
            }
        }
        catch (NoSuchAlgorithmException e) {
            if (log.isErrorEnabled()) {
                log.error("Unsupported algorithm MD5", e);
            }
        }

        return hexString.toString();
    }
}